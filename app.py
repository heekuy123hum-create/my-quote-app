# --- 3. ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏™‡∏£‡πâ‡∏≤‡∏á PDF (‡∏î‡∏µ‡πÑ‡∏ã‡∏ô‡πå‡∏ï‡∏≤‡∏°‡πÑ‡∏ü‡∏•‡πå‡∏ó‡∏µ‡πà‡∏û‡∏µ‡πà‡∏™‡πà‡∏á‡∏°‡∏≤) ---
def create_pdf(doc_no, cust_name, cust_addr, df_items, subtotal, vat, grand_total, sigs):
    pdf = FPDF()
    pdf.add_page()
    
    # ‡∏ü‡∏≠‡∏ô‡∏ï‡πå‡πÑ‡∏ó‡∏¢‡∏ó‡∏µ‡πà‡∏û‡∏µ‡πà‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î
    font_path = "THSarabunNew.ttf"
    if os.path.exists(font_path):
        pdf.add_font('THSarabun', '', font_path)
        pdf.add_font('THSarabun', 'B', font_path)
        use_font = 'THSarabun'
    else:
        pdf.set_font("Arial", '', 14)
        use_font = 'Arial'

    # --- ‡πÇ‡∏•‡πÇ‡∏Å‡πâ‡∏°‡∏∏‡∏°‡∏ã‡πâ‡∏≤‡∏¢‡∏ö‡∏ô ---
    logo_file = find_logo()
    if logo_file:
        pdf.image(logo_file, x=10, y=10, w=35)
        pdf.set_y(35) 
    else:
        pdf.set_y(20)

    # ‡∏´‡∏±‡∏ß‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£ (‡∏ä‡∏¥‡∏î‡∏Ç‡∏ß‡∏≤‡∏ï‡∏≤‡∏°‡πÑ‡∏ü‡∏•‡πå‡∏ï‡πâ‡∏ô‡∏â‡∏ö‡∏±‡∏ö)
    pdf.set_font(use_font, 'B', 24)
    pdf.cell(0, 10, "‡πÉ‡∏ö‡πÄ‡∏™‡∏ô‡∏≠‡∏£‡∏≤‡∏Ñ‡∏≤ (QUOTATION)", 0, 1, 'R')
    pdf.ln(5)

    # ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤
    pdf.set_font(use_font, 'B', 14)
    pdf.cell(120, 8, "‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤ (Customer Details):", 0, 0)
    pdf.cell(70, 8, f"‡πÄ‡∏•‡∏Ç‡∏ó‡∏µ‡πà: {doc_no}", 0, 1, 'R')

    pdf.set_font(use_font, '', 14)
    pdf.cell(120, 8, f"‡∏ä‡∏∑‡πà‡∏≠‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤: {cust_name if cust_name else ''}", 0, 0)
    pdf.cell(70, 8, f"‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà: {datetime.now().strftime('%d/%m/%Y')}", 0, 1, 'R')
    pdf.multi_cell(0, 8, f"‡∏ó‡∏µ‡πà‡∏≠‡∏¢‡∏π‡πà: {cust_addr if cust_addr else ''}")
    pdf.ln(5)

    # --- ‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£ (‡∏ï‡∏µ‡πÄ‡∏™‡πâ‡∏ô 20 ‡πÅ‡∏ñ‡∏ß‡πÉ‡∏´‡πâ‡πÄ‡∏ï‡πá‡∏°‡πÅ‡∏ú‡πà‡∏ô) ---
    pdf.set_fill_color(240, 240, 240)
    pdf.set_font(use_font, 'B', 12)
    h = 9
    # ‡∏Å‡∏≥‡∏´‡∏ô‡∏î‡∏Ñ‡∏ß‡∏≤‡∏°‡∏Å‡∏ß‡πâ‡∏≤‡∏á‡∏Ñ‡∏≠‡∏•‡∏±‡∏°‡∏ô‡πå (‡∏ï‡∏≤‡∏°‡∏ü‡∏¥‡∏•‡∏î‡πå‡πÉ‡∏ô quotation_ui.py)
    w = [10, 25, 65, 15, 15, 25, 35] 
    headers = ["‡∏•‡∏≥‡∏î‡∏±‡∏ö", "‡∏£‡∏´‡∏±‡∏™‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤", "‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤", "‡∏à‡∏≥‡∏ô‡∏ß‡∏ô", "‡∏´‡∏ô‡πà‡∏ß‡∏¢", "‡∏£‡∏≤‡∏Ñ‡∏≤/‡∏´‡∏ô‡πà‡∏ß‡∏¢", "‡∏£‡∏ß‡∏°‡πÄ‡∏á‡∏¥‡∏ô"]
    
    for i in range(len(headers)):
        pdf.cell(w[i], h, headers[i], 1, 0, 'C', True)
    pdf.ln()

    pdf.set_font(use_font, '', 13)
    for i in range(20):
        if i < len(df_items):
            row = df_items.iloc[i]
            # ‡∏Å‡∏£‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•: ‡∏ñ‡πâ‡∏≤‡πÄ‡∏õ‡πá‡∏ô 0 ‡∏´‡∏£‡∏∑‡∏≠‡∏ß‡πà‡∏≤‡∏á ‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô‡∏ä‡πà‡∏≠‡∏á‡∏Ç‡∏≤‡∏ß
            d = [
                str(i + 1),
                str(row.get('‡∏£‡∏´‡∏±‡∏™‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤', '')),
                str(row.get('‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£', '')),
                f"{float(row.get('‡∏à‡∏≥‡∏ô‡∏ß‡∏ô', 0)):,.0f}" if float(row.get('‡∏à‡∏≥‡∏ô‡∏ß‡∏ô', 0)) > 0 else "",
                str(row.get('‡∏´‡∏ô‡πà‡∏ß‡∏¢', '')),
                f"{float(row.get('‡∏£‡∏≤‡∏Ñ‡∏≤', 0)):,.0f}" if float(row.get('‡∏£‡∏≤‡∏Ñ‡∏≤', 0)) > 0 else "",
                f"{float(row.get('‡∏£‡∏ß‡∏°‡πÄ‡∏á‡∏¥‡∏ô', 0)):,.0f}" if float(row.get('‡∏£‡∏ß‡∏°‡πÄ‡∏á‡∏¥‡∏ô', 0)) > 0 else ""
            ]
        else:
            d = ["", "", "", "", "", "", ""]

        for j in range(len(d)):
            align = 'C' if j in [0, 1, 3, 4] else ('L' if j == 2 else 'R')
            pdf.cell(w[j], h, d[j], 1, 0, align)
        pdf.ln()

    # ‡∏™‡∏£‡∏∏‡∏õ‡πÄ‡∏á‡∏¥‡∏ô
    pdf.ln(3)
    pdf.set_font(use_font, 'B', 14)
    pdf.cell(sum(w[:-1]), 8, "‡∏£‡∏ß‡∏°‡πÄ‡∏á‡∏¥‡∏ô‡∏™‡∏∏‡∏ó‡∏ò‡∏¥ (Sub Total):", 0, 0, 'R')
    pdf.cell(w[-1], 8, f"{subtotal:,.0f}", 'B', 1, 'R')
    pdf.cell(sum(w[:-1]), 8, "‡∏†‡∏≤‡∏©‡∏µ‡∏°‡∏π‡∏•‡∏Ñ‡πà‡∏≤‡πÄ‡∏û‡∏¥‡πà‡∏° (VAT 7%):", 0, 0, 'R')
    pdf.cell(w[-1], 8, f"{vat:,.0f}", 'B', 1, 'R')
    pdf.set_font(use_font, 'B', 16)
    pdf.cell(sum(w[:-1]), 10, "‡∏¢‡∏≠‡∏î‡∏£‡∏ß‡∏°‡∏™‡∏∏‡∏ó‡∏ò‡∏¥ (Grand Total):", 0, 0, 'R')
    pdf.set_text_color(200, 0, 0)
    pdf.cell(w[-1], 10, f"{grand_total:,.0f} THB", 'B', 1, 'R')
    pdf.set_text_color(0, 0, 0)

    # --- ‡∏™‡πà‡∏ß‡∏ô‡πÄ‡∏ã‡πá‡∏ô‡∏ä‡∏∑‡πà‡∏≠ 3 ‡∏ä‡πà‡∏≠‡∏á (‡∏ï‡∏≤‡∏°‡πÑ‡∏ü‡∏•‡πå PDF Chrome) ---
    pdf.ln(10)
    sig_y = pdf.get_y()
    pdf.set_font(use_font, '', 12)
    labels = ["‡∏ú‡∏π‡πâ‡∏≠‡∏ô‡∏∏‡∏°‡∏±‡∏ï‡∏¥‡∏™‡∏±‡πà‡∏á‡∏ã‡∏∑‡πâ‡∏≠", "‡∏û‡∏ô‡∏±‡∏Å‡∏á‡∏≤‡∏ô‡∏Ç‡∏≤‡∏¢", "‡∏ú‡∏π‡πâ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏ù‡πà‡∏≤‡∏¢‡∏Ç‡∏≤‡∏¢"]
    for i in range(3):
        pdf.set_xy(10 + (i*65), sig_y)
        pdf.cell(60, 6, "..........................................", 0, 1, 'C')
        pdf.set_x(10 + (i*65))
        pdf.cell(60, 6, labels[i], 0, 1, 'C')
        pdf.set_x(10 + (i*65))
        pdf.cell(60, 6, f"({sigs[i]})", 0, 1, 'C')

    # ‚úÖ ‡πÅ‡∏Å‡πâ‡πÑ‡∏Ç‡∏à‡∏∏‡∏î‡∏ï‡∏≤‡∏¢‡∏ó‡∏µ‡πà‡∏ó‡∏≥‡πÉ‡∏´‡πâ‡πÄ‡∏Å‡∏¥‡∏î Error: ‡πÅ‡∏õ‡∏•‡∏á bytearray ‡πÄ‡∏õ‡πá‡∏ô bytes
    return bytes(pdf.output())

# --- 4. STREAMLIT UI ---
st.title("üìÑ ‡∏£‡∏∞‡∏ö‡∏ö‡πÉ‡∏ö‡πÄ‡∏™‡∏ô‡∏≠‡∏£‡∏≤‡∏Ñ‡∏≤ (Full Design)")

tab1, tab2, tab3 = st.tabs(["üìù ‡∏™‡∏£‡πâ‡∏≤‡∏á‡πÄ‡∏≠‡∏Å‡∏™‡∏≤‡∏£", "üë• ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤", "üì¶ ‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤"])

with tab1:
    # ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤‡∏°‡∏≤‡πÅ‡∏™‡∏î‡∏á‡πÉ‡∏ô Selectbox
    res_c = conn.table("customers").select("*").execute()
    df_c = pd.DataFrame(res_c.data) if res_c.data else pd.DataFrame()
    
    col_left, col_right = st.columns(2)
    with col_left:
        st.subheader("‡∏™‡πà‡∏ß‡∏ô‡∏ó‡∏µ‡πà 1: ‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤")
        c_list = ["-- ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å --"] + (df_c.iloc[:, 0].tolist() if not df_c.empty else [])
        sid = st.selectbox("‡∏£‡∏´‡∏±‡∏™‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤", options=c_list)
        info = df_c[df_c.iloc[:, 0] == sid].iloc[0] if sid != "-- ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å --" else {}
        name = st.text_input("‡∏ä‡∏∑‡πà‡∏≠‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤", value=info.get('name', ''))
        addr = st.text_area("‡∏ó‡∏µ‡πà‡∏≠‡∏¢‡∏π‡πà‡∏à‡∏±‡∏î‡∏™‡πà‡∏á", value=info.get('address', ''), height=100)

    with col_right:
        st.subheader("‡∏™‡πà‡∏ß‡∏ô‡∏ó‡∏µ‡πà 2: ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡∏´‡∏ô‡πâ‡∏≤‡πÄ‡∏ã‡πá‡∏ô‡∏ä‡∏∑‡πà‡∏≠")
        s1 = st.text_input("‡∏ä‡∏∑‡πà‡∏≠‡∏ú‡∏π‡πâ‡∏≠‡∏ô‡∏∏‡∏°‡∏±‡∏ï‡∏¥", "....................")
        s2 = st.text_input("‡∏ä‡∏∑‡πà‡∏≠‡∏û‡∏ô‡∏±‡∏Å‡∏á‡∏≤‡∏ô‡∏Ç‡∏≤‡∏¢", "....................")
        s3 = st.text_input("‡∏ä‡∏∑‡πà‡∏≠‡∏ú‡∏π‡πâ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£", "....................")
        dno = st.text_input("‡πÄ‡∏•‡∏Ç‡∏ó‡∏µ‡πà", f"QT-{datetime.now().strftime('%y%m%d-%H')}")
        v_on = st.checkbox("‡∏Ñ‡∏¥‡∏î VAT 7%", value=True)

    st.divider()
    
    # ‡∏ï‡∏≤‡∏£‡∏≤‡∏á‡∏Å‡∏£‡∏≠‡∏Å‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤ (20 ‡πÅ‡∏ñ‡∏ß)
    grid_init = [{"‡∏£‡∏´‡∏±‡∏™‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤": "", "‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£": "", "‡∏à‡∏≥‡∏ô‡∏ß‡∏ô": 0, "‡∏´‡∏ô‡πà‡∏ß‡∏¢": "", "‡∏£‡∏≤‡∏Ñ‡∏≤": 0}] * 20
    edited = st.data_editor(grid_init, num_rows="dynamic", use_container_width=True)
    
    df_grid = pd.DataFrame(edited)
    df_grid['‡∏£‡∏ß‡∏°‡πÄ‡∏á‡∏¥‡∏ô'] = pd.to_numeric(df_grid['‡∏à‡∏≥‡∏ô‡∏ß‡∏ô'], 0) * pd.to_numeric(df_grid['‡∏£‡∏≤‡∏Ñ‡∏≤'], 0)
    
    sub = int(df_grid['‡∏£‡∏ß‡∏°‡πÄ‡∏á‡∏¥‡∏ô'].sum())
    v_amt = int(sub * 0.07) if v_on else 0
    grand = sub + v_amt

    if st.button("‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡πÅ‡∏•‡∏∞‡∏™‡∏£‡πâ‡∏≤‡∏á PDF", type="primary"):
        try:
            # ‡πÄ‡∏£‡∏µ‡∏¢‡∏Å‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ó‡∏µ‡πà‡πÅ‡∏Å‡πâ Error Bytearray ‡πÅ‡∏•‡πâ‡∏ß
            pdf_data = create_pdf(dno, name, addr, df_grid, sub, v_amt, grand, [s1, s2, s3])
            st.download_button("üì• ‡∏î‡∏≤‡∏ß‡∏ô‡πå‡πÇ‡∏´‡∏•‡∏î‡πÉ‡∏ö‡πÄ‡∏™‡∏ô‡∏≠‡∏£‡∏≤‡∏Ñ‡∏≤", data=pdf_data, file_name=f"{dno}.pdf", mime="application/pdf")
        except Exception as e:
            st.error(f"‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î: {e}")
