# ‡∏î‡∏∂‡∏á‡∏Ñ‡πà‡∏≤ Config ‡∏à‡∏≤‡∏Å Environment Variables (‡πÉ‡∏ô Render)
MY_SUPABASE_URL = os.environ.get("SUPABASE_URL")
